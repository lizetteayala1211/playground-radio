  window.onload = function (event) {

    var myitem = document.querySelectorAll(".hover-background");
    for(h=0; h<myitem.length; h++){
        myitem[h].onclick = function(e){
            var myitem = document.querySelectorAll(".hover-background");

            for(h=0; h<myitem.length; h++){
                myitem[h].classList.remove("bordered");
            }

            this.classList.add("bordered");

        };
    }
};

